"""
Generates a Python feature-definition module from a :class:`FeastDefinitionSpec`.

The output is a standalone ``.py`` file that can be placed in a Feast repo and
applied with ``feast apply``.  Every ``OnDemandFeatureView`` that carries
aggregations receives an auto-generated passthrough UDF.
"""

from __future__ import annotations

import re
import textwrap
from typing import Any, Dict, List, Optional, Set, Tuple

from jinja2 import Environment, StrictUndefined

from .parser import (
    AggregationSpec,
    DataSourceSpec,
    EntitySpec,
    FeastDefinitionSpec,
    FeatureViewSpec,
    FieldSpec,
    OnDemandFeatureViewSpec,
    YamlDefinitionParser,
)

# ── constants ────────────────────────────────────────────────────────────────

# Maps source type → (import module, class name) for generated code.
_DS_IMPORT: Dict[str, Tuple[str, str]] = {
    "bigquery": ("feast.infra.offline_stores.bigquery", "BigQuerySource"),
    "snowflake": ("feast.infra.offline_stores.snowflake", "SnowflakeSource"),
    "redshift": ("feast.infra.offline_stores.redshift", "RedshiftSource"),
    "file": ("feast.infra.offline_stores.file", "FileSource"),
    "push": ("feast.data_source", "PushSource"),
}

_DURATION_UNITS = {"d": "days", "h": "hours", "m": "minutes", "s": "seconds"}
_DURATION_RE = re.compile(r"^(\d+)([dhms])$")

