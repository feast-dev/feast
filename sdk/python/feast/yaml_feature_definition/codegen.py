# ── helpers ──────────────────────────────────────────────────────────────────


def _var(name: str) -> str:
    """Convert an arbitrary name to a valid Python variable name."""
    return re.sub(r"\W+", "_", name).strip("_")


def _topy(value: Any) -> str:
    """Render a Python literal for a string or other scalar value."""
    if isinstance(value, str):
        escaped = value.replace("\\", "\\\\").replace('"', '\\"')
        return f'"{escaped}"'
    return repr(value)


def _todict(d: Dict[str, str]) -> str:
    pairs = ", ".join(f'"{k}": "{v}"' for k, v in d.items())
    return "{" + pairs + "}"


def _dtype_expr(dtype: str) -> str:
    """
    Convert a dtype string to its Python expression, e.g.
    ``"Array(Int64)"`` → ``"Array(Int64)"``, ``"int64"`` → ``"Int64"``.
    """
    key = dtype.strip()
    m = re.match(r"^Array\((.+)\)$", key, re.IGNORECASE)
    if m:
        return f"Array({_dtype_expr(m.group(1))})"
    # Normalise primitive names to title-case canonical form.
    _canon = {
        "string": "String", "str": "String",
        "int32": "Int32",
        "int64": "Int64", "int": "Int64", "integer": "Int64",
        "float32": "Float32",
        "float64": "Float64", "float": "Float64", "double": "Float64",
        "bool": "Bool", "boolean": "Bool",
        "bytes": "Bytes",
        "unixtimestamp": "UnixTimestamp", "unix_timestamp": "UnixTimestamp",
        "json": "Json",
        "decimal": "Decimal",
    }
    return _canon.get(key.lower(), key)


def _collect_types(dtype: str, acc: Set[str]) -> None:
    """Recursively collect all Feast type names required for import."""
    m = re.match(r"^Array\((.+)\)$", dtype.strip(), re.IGNORECASE)
    if m:
        acc.add("Array")
        _collect_types(m.group(1), acc)
    else:
        acc.add(_dtype_expr(dtype))


def _duration_expr(s: str) -> str:
    """``"30d"`` → ``"timedelta(days=30)"``."""
    m = _DURATION_RE.match(s.strip())
    if not m:
        raise ValueError(f"Invalid duration {s!r}.")
    value, unit = int(m.group(1)), m.group(2)
    return f"timedelta({_DURATION_UNITS[unit]}={value})"


# ── view-model builders (spec → template-friendly dicts) ─────────────────────


def _build_field(f: FieldSpec) -> Dict[str, Any]:
    return {
        "name": f.name,
        "dtype_expr": _dtype_expr(f.dtype),
        "description": f.description,
        "tags": f.tags,
    }


def _build_entity(e: EntitySpec) -> Dict[str, Any]:
    return {
        "var": _var(e.name),
        "name": e.name,
        "join_key": e.join_key or e.name,
        "description": e.description,
        "tags": e.tags,
        "owner": e.owner,
    }


def _build_data_source(ds: DataSourceSpec) -> Dict[str, Any]:
    ds_type = ds.type.lower()
    if ds_type not in _DS_IMPORT:
        raise ValueError(
            f"Unsupported data source type {ds.type!r}. "
            f"Supported: {sorted(_DS_IMPORT)}."
        )
    _, cls_name = _DS_IMPORT[ds_type]
    return {
        "var": _var(ds.name),
        "name": ds.name,
        "cls": cls_name,
        "timestamp_field": ds.timestamp_field,
        "created_timestamp_column": ds.created_timestamp_column,
        "field_mapping": ds.field_mapping,
        "config": ds.config,
        "description": ds.description,
        "tags": ds.tags,
        "owner": ds.owner,
    }


def _build_feature_view(
    fv: FeatureViewSpec, entity_vars: Dict[str, str], source_vars: Dict[str, str]
) -> Dict[str, Any]:
    return {
        "var": _var(fv.name),
        "name": fv.name,
        "entity_vars": [entity_vars[n] for n in fv.entities if n in entity_vars],
        "source_var": source_vars.get(fv.source) if fv.source else None,
        "ttl_days": fv.ttl_days,
        "online": fv.online,
        "offline": fv.offline,
        "description": fv.description,
        "tags": fv.tags,
        "owner": fv.owner,
        "schema": [_build_field(f) for f in fv.schema],
    }


def _build_aggregation(a: AggregationSpec) -> Dict[str, Any]:
    return {
        "column": a.column,
        "function": a.function,
        "time_window_expr": _duration_expr(a.time_window),
        "slide_interval_expr": _duration_expr(a.slide_interval) if a.slide_interval else None,
        "name": a.name,
    }


def _build_odfv(
    odfv: OnDemandFeatureViewSpec,
    entity_vars: Dict[str, str],
    fv_vars: Dict[str, str],
) -> Dict[str, Any]:
    return {
        "var": _var(odfv.name),
        "name": odfv.name,
        "entity_vars": [entity_vars[n] for n in odfv.entities if n in entity_vars],
        "source_vars": [fv_vars[n] for n in odfv.sources if n in fv_vars],
        "mode": odfv.mode,
        "write_to_online_store": odfv.write_to_online_store,
        "description": odfv.description,
        "tags": odfv.tags,
        "owner": odfv.owner,
        "aggregations": [_build_aggregation(a) for a in odfv.aggregations],
        "schema": [_build_field(f) for f in odfv.schema],
    }


# ── generator ────────────────────────────────────────────────────────────────


class YamlCodeGenerator:
    """Generates a Python feature-definition module from a YAML spec."""

    def generate_from_file(self, yaml_path: str) -> str:
        spec = YamlDefinitionParser().parse_file(yaml_path)
        return self.generate(spec, yaml_path=yaml_path)

    def generate_from_string(self, yaml_content: str, yaml_path: str = "<string>") -> str:
        spec = YamlDefinitionParser().parse_string(yaml_content)
        return self.generate(spec, yaml_path=yaml_path)

    def generate(self, spec: FeastDefinitionSpec, yaml_path: str = "") -> str:
        entity_vars = {e.name: _var(e.name) for e in spec.entities}
        source_vars = {ds.name: _var(ds.name) for ds in spec.data_sources}
        fv_vars = {fv.name: _var(fv.name) for fv in spec.feature_views}

        entities = [_build_entity(e) for e in spec.entities]
        data_sources = [_build_data_source(ds) for ds in spec.data_sources]
        feature_views = [
            _build_feature_view(fv, entity_vars, source_vars)
            for fv in spec.feature_views
        ]
        on_demand_feature_views = [
            _build_odfv(o, entity_vars, fv_vars)
            for o in spec.on_demand_feature_views
        ]

        # Collect all Feast types referenced across every schema.
        type_set: Set[str] = set()
        for fv in spec.feature_views:
            for f in fv.schema:
                _collect_types(f.dtype, type_set)
        for o in spec.on_demand_feature_views:
            for f in o.schema:
                _collect_types(f.dtype, type_set)

        # Data-source imports (deduplicated, ordered).
        ds_type_set = {ds.type.lower() for ds in spec.data_sources}
        ds_imports = sorted(
            {_DS_IMPORT[t] for t in ds_type_set if t in _DS_IMPORT},
            key=lambda x: x[1],
        )

        # Core feast imports.
        feast_cls: List[str] = []
        if entities:
            feast_cls.append("Entity")
        if feature_views:
            feast_cls.append("FeatureView")
        if feature_views or on_demand_feature_views:
            feast_cls.append("Field")
        if on_demand_feature_views:
            feast_cls.append("OnDemandFeatureView")

        has_aggregations = any(
            len(o.aggregations) > 0 for o in spec.on_demand_feature_views
        )

        env = Environment(undefined=StrictUndefined, keep_trailing_newline=True)
        env.filters["topy"] = _topy
        env.filters["todict"] = _todict

        rendered = env.from_string(_TEMPLATE).render(
            yaml_path=yaml_path,
            feast_imports=sorted(feast_cls),
            has_aggregations=has_aggregations,
            ds_imports=ds_imports,
            type_imports=sorted(type_set),
            entities=entities,
            data_sources=data_sources,
            feature_views=feature_views,
            on_demand_feature_views=on_demand_feature_views,
        )
        # Collapse runs of more than two blank lines.
        return re.sub(r"\n{3,}", "\n\n", rendered).strip() + "\n"


def generate_feast_code(
    yaml_path: str,
    *,
    output_path: Optional[str] = None,
) -> str:
    """
    Convenience function: parse *yaml_path* and return generated Python source.

    If *output_path* is given the result is also written there.
    """
    code = YamlCodeGenerator().generate_from_file(yaml_path)
    if output_path:
        with open(output_path, "w") as fh:
            fh.write(code)
    return code
